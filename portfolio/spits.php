Description: Worked with local punk band to design and develop a WordPress-based e-commerce website to promote music, sell merchandise and track upcoming shows.

Technology: WordPress, HTML, CSS, JavaScript

Challenges / Solutions: The goals was building a website that was easy for the band to update, sell merchandise and promote upcoming shows. I built a responsive WordPress website utilizing the Woo Commerce plugin for merchandise management, the Songify API to keep updated events and Spotify API player to play their music.

